// Enemy ∑£¥˝Ω∫∆˘
void Map::SpawnEnemy()
{
	srand(time(nullptr));
	int randX = rand() % WIDTH + 1;
	int randY = rand() % HEIGHT + 1;

	while (true)
	{
		if (arrMap[randY][randX] != ENEMY && arrMap[randY][randX] != PLAYER && arrMap[randY][randX] != WALL && arrMap[randY][randX] != BODY)
		{
			arrMap[randY][randX] = ENEMY;
			break;
		}
		else
		{
			randX = rand() % WIDTH + 1;
			randY = rand() % HEIGHT + 1;
		}
	}
}
